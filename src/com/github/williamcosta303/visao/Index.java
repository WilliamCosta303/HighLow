    private int gerarDinheiroEstranho(){
        int valorFinal = this.geraNumeroAleatorio(1, 620);
        if(valorFinal < 200){
            do{
                valorFinal+= this.geraNumeroAleatorio(1, 20);
            }while(valorFinal < 200);
        }
        
        return valorFinal;
    }
    
