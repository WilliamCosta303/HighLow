    private String getNaipe(int codigo, boolean isCodigo){
        String resultado;
        switch(codigo){
            case 1:
                if(isCodigo){
                    resultado = "ouro";
                } else {
                    resultado = " de ouros";
                }
                break;
            case 2:
                if(isCodigo){
                    resultado = "paus";
                } else {
                    resultado = " de paus";
                }
                break;
            case 3:
                if(isCodigo){
                    resultado = "espa";
                } else {
                    resultado = " de espadas";
                }
                break;
            case 4:
                if(isCodigo){
                    resultado = "copa";
                } else {
                    resultado = " de copas";
                }
                break;
            default:
                resultado = "bugou pra caralho";
                break;
        }
        return resultado;
    }
    
